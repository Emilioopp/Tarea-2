function stringMayusculas(string){
    return string.toUpperCase();
}
console.log(stringMayusculas("hola mundo"));

function stringMinusculas(string){
    return string.toLowerCase();
}
console.log(stringMinusculas("HOLA MUNDO"));

function resta(a, b){
    return a - b;
}
console.log(resta(2, 3));

function divide(a, b){
    return a / b;
}
console.log(divide(10, 5));

function multiplica(a, b){
    return a * b;
}
console.log(multiplica(3, 3));

function longitud(string){
    return string.length;
}
console.log(longitud("hola mundo"));